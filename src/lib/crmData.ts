// 统计数据类型
export interface CrmStats {
  totalCustomers: number;
  completedProjects: number;
  pendingFollowUp: number;
  inService: number;
  industryDistribution: { industry: string; count: number }[];
  lastUpdated: Date;
}

// 计算统计数据
export function calculateCrmStats(customers: Customer[]): CrmStats {
  const totalCustomers = customers.length;
  const completedProjects = customers.filter(c => c.客户周期 === "完成").length;
  const pendingFollowUp = customers.filter(c => c.客户周期 === "待跟进").length;
  const inService = customers.filter(c => c.客户周期 === "服务中").length;

  // 行业分布统计
  const industryMap = new Map<string, number>();
  customers.forEach(c => {
    industryMap.set(c.行业, (industryMap.get(c.行业) || 0) + 1);
  });
  const industryDistribution = Array.from(industryMap.entries())
    .map(([industry, count]) => ({ industry, count }))
    .sort((a, b) => b.count - a.count);

  return {
    totalCustomers,
    completedProjects,
    pendingFollowUp,
    inService,
    industryDistribution,
    lastUpdated: new Date(),
  };
}
